char	*ft_strchr(const char *str, int c)
{
	int		i;

	i = 0;
	if (!(c >= 0 && c <= 127))
		return ((char *) str);
	while (str[i] || c == 0)
	{
		if (str[i] == c)
		{
			str += i;
			return ((char *) str);
		}
		i++;
	}
	return (0);
}
