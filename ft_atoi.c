#include "libft.h"

int	ft_atoi(const char *s)
{
	int	val;
	int	i;
	int	sign;

	val = 0;
	sign = 1;
	i = 0;
	while (s[i] == 32 || (s[i] >= 9 && s[i] <= 13))
		i++;
	if (s[i] == '-' || s[i] == '+')
		if (s[i++] == '-')
			sign = -1;
	while (s[i] > 47 && s[i] < 58)
	{	
		val = 10 * val + (s[i] - 48);
		i++;
	}
	return (sign * val);
}
